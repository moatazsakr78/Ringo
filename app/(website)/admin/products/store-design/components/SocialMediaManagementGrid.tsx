// Single draggable item
function SortableSocialMediaItem({
  link,
  isSelected,
  isDragMode,
  onSelect,
  onToggleVisibility,
  onDelete,
  onEdit
}: {
  link: SocialMediaLink;
  isSelected: boolean;
  isDragMode: boolean;
  onSelect: () => void;
  onToggleVisibility: () => void;
  onDelete: () => void;
  onEdit: () => void;
}) {
  const {
    attributes,
    listeners,
    setNodeRef,
    transform,
    transition,
    isDragging
  } = useSortable({ id: link.id, disabled: !isDragMode });

  const style = {
    transform: CSS.Transform.toString(transform),
    transition,
    opacity: isDragging ? 0.5 : 1,
    zIndex: isDragging ? 1000 : 1
  };

  const platformColor = PLATFORM_COLORS[link.platform_icon] || PLATFORM_COLORS.custom;
  const platformInfo = PLATFORM_ICONS[link.platform_icon] || PLATFORM_ICONS.custom;

  return (
    <div
      ref={setNodeRef}
      style={style}
      className={`relative bg-white rounded-xl shadow-sm border-2 transition-all duration-200 ${
        isSelected
          ? 'border-red-500 ring-2 ring-red-200'
          : 'border-gray-200 hover:border-gray-300'
      } ${isDragMode ? 'cursor-grab active:cursor-grabbing' : 'cursor-pointer'} ${
        link.is_active ? '' : 'opacity-50'
      }`}
      onClick={() => !isDragMode && onSelect()}
      {...(isDragMode ? { ...attributes, ...listeners } : {})}
    >
      {/* Drag handle indicator */}
      {isDragMode && (
        <div className="absolute top-2 right-2 text-gray-400">
          <svg className="w-5 h-5" fill="currentColor" viewBox="0 0 20 20">
            <path d="M7 2a2 2 0 1 0 .001 4.001A2 2 0 0 0 7 2zm0 6a2 2 0 1 0 .001 4.001A2 2 0 0 0 7 8zm0 6a2 2 0 1 0 .001 4.001A2 2 0 0 0 7 14zm6-8a2 2 0 1 0-.001-4.001A2 2 0 0 0 13 6zm0 2a2 2 0 1 0 .001 4.001A2 2 0 0 0 13 8zm0 6a2 2 0 1 0 .001 4.001A2 2 0 0 0 13 14z" />
          </svg>
        </div>
      )}

      {/* Hidden badge */}
      {!link.is_active && (
        <div className="absolute top-2 left-2 bg-gray-500 text-white text-xs px-2 py-1 rounded-full">
          مخفي
        </div>
      )}

      {/* Selection indicator */}
      {isSelected && !isDragMode && (
        <div className="absolute top-2 right-2 w-6 h-6 bg-red-500 rounded-full flex items-center justify-center">
          <svg className="w-4 h-4 text-white" fill="none" stroke="currentColor" viewBox="0 0 24 24">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M5 13l4 4L19 7" />
          </svg>
        </div>
      )}

      {/* Content */}
      <div className="p-4 flex flex-col items-center text-center">
        {/* Platform icon */}
        <div
          className="w-16 h-16 rounded-xl flex items-center justify-center mb-3"
          style={{ backgroundColor: `${platformColor}15` }}
        >
          {link.custom_icon_url ? (
            <img
              src={link.custom_icon_url}
              alt={link.platform}
              className="w-10 h-10 object-contain"
            />
          ) : (
            <div style={{ color: platformColor }}>
              <PlatformIconSVG platform={link.platform_icon} className="w-10 h-10" />
            </div>
          )}
        </div>

        {/* Platform name */}
        <h3 className="font-semibold text-gray-800 mb-1">{link.platform}</h3>

        {/* Link preview */}
        <p className="text-xs text-gray-500 truncate max-w-full">
          {link.whatsapp_number ? `wa.me/${link.whatsapp_number}` : link.link_url}
        </p>
      </div>

      {/* Action buttons - only show when selected and not in drag mode */}
      {isSelected && !isDragMode && (
        <div className="absolute bottom-0 left-0 right-0 bg-gray-50 border-t border-gray-200 p-2 rounded-b-xl flex justify-center gap-2">
          <button
            onClick={(e) => {
              e.stopPropagation();
              onEdit();
            }}
            className="p-2 text-blue-600 hover:bg-blue-50 rounded-lg transition-colors"
            title="تعديل"
          >
            <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M11 5H6a2 2 0 00-2 2v11a2 2 0 002 2h11a2 2 0 002-2v-5m-1.414-9.414a2 2 0 112.828 2.828L11.828 15H9v-2.828l8.586-8.586z" />
            </svg>
          </button>
          <button
            onClick={(e) => {
              e.stopPropagation();
              onToggleVisibility();
            }}
            className={`p-2 rounded-lg transition-colors ${
              link.is_active
                ? 'text-orange-600 hover:bg-orange-50'
                : 'text-green-600 hover:bg-green-50'
            }`}
            title={link.is_active ? 'إخفاء' : 'إظهار'}
          >
            {link.is_active ? (
              <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M13.875 18.825A10.05 10.05 0 0112 19c-4.478 0-8.268-2.943-9.543-7a9.97 9.97 0 011.563-3.029m5.858.908a3 3 0 114.243 4.243M9.878 9.878l4.242 4.242M9.88 9.88l-3.29-3.29m7.532 7.532l3.29 3.29M3 3l3.59 3.59m0 0A9.953 9.953 0 0112 5c4.478 0 8.268 2.943 9.543 7a10.025 10.025 0 01-4.132 5.411m0 0L21 21" />
              </svg>
            ) : (
              <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M15 12a3 3 0 11-6 0 3 3 0 016 0z" />
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M2.458 12C3.732 7.943 7.523 5 12 5c4.478 0 8.268 2.943 9.542 7-1.274 4.057-5.064 7-9.542 7-4.477 0-8.268-2.943-9.542-7z" />
              </svg>
            )}
          </button>
          <button
            onClick={(e) => {
              e.stopPropagation();
              onDelete();
            }}
            className="p-2 text-red-600 hover:bg-red-50 rounded-lg transition-colors"
            title="حذف"
          >
            <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M19 7l-.867 12.142A2 2 0 0116.138 21H7.862a2 2 0 01-1.995-1.858L5 7m5 4v6m4-6v6m1-10V4a1 1 0 00-1-1h-4a1 1 0 00-1 1v3M4 7h16" />
            </svg>
          </button>
        </div>
      )}
    </div>
  );
}

