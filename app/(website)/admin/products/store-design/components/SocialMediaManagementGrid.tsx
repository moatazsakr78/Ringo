export default function SocialMediaManagementGrid({
  links,
  selectedLinkId,
  isDragMode,
  onLinkSelect,
  onToggleVisibility,
  onDelete,
  onEdit
}: SocialMediaManagementGridProps) {
  if (links.length === 0) {
    return (
      <div className="text-center py-12 text-gray-500">
        <svg
          className="w-16 h-16 mx-auto mb-4 text-gray-300"
          fill="none"
          stroke="currentColor"
          viewBox="0 0 24 24"
        >
          <path
            strokeLinecap="round"
            strokeLinejoin="round"
            strokeWidth={1.5}
            d="M8.684 13.342C8.886 12.938 9 12.482 9 12c0-.482-.114-.938-.316-1.342m0 2.684a3 3 0 110-2.684m0 2.684l6.632 3.316m-6.632-6l6.632-3.316m0 0a3 3 0 105.367-2.684 3 3 0 00-5.367 2.684zm0 9.316a3 3 0 105.368 2.684 3 3 0 00-5.368-2.684z"
          />
        </svg>
        <p className="text-lg font-medium">لا توجد روابط سوشيال ميديا</p>
        <p className="text-sm mt-1">اضغط على زر "إضافة" لإضافة رابط جديد</p>
      </div>
    );
  }

  return (
    <div className="grid grid-cols-2 sm:grid-cols-3 md:grid-cols-4 lg:grid-cols-5 gap-4">
      {links.map((link) => (
        <SortableSocialMediaItem
          key={link.id}
          link={link}
          isSelected={selectedLinkId === link.id}
          isDragMode={isDragMode}
          onSelect={() => onLinkSelect(link.id)}
          onToggleVisibility={() => onToggleVisibility(link.id)}
          onDelete={() => onDelete(link.id)}
          onEdit={() => onEdit(link)}
        />
      ))}
    </div>
  );
}
